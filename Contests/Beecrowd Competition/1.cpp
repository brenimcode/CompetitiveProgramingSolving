#include <bits/stdc++.h>

using namespace std;


int main(){
    int a,b,c;

    cin >> a >> b >> c;
    if(a != 1 && b != 1 && c != 1){
        cout << "1\n";
    }
    else if(a != 2 && b != 2 && c != 2){
        cout << "2\n";
    }
    else if(a != 3 && b != 3 && c != 3){
        cout << "3\n";
    }

    return 0;

}


